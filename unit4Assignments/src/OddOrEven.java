import static java.lang.System.*;

public class OddOrEven
{
	public static boolean isOdd(int j)
	{
		if(j%2==1) {
		return true;
		}else {
			return false;
		}
	}
	public static boolean isEven(int j)
	{
		if(j%2==0) {
			return true;
			}else {
				return false;
			}
	}	
}